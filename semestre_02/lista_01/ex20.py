# Função variaveis_dependentes()
# Crie uma variável que guarde o valor 10. Depois crie outra variável que receba o valor da primeira somado com 5 e exiba ambas.

def variaveis_dependentes():

  variavel_a = 10
  variavel_b = variavel_a + 5

  print(f"O valor da primeira variável é: {variavel_a}")
  print(f"O valor da segunda variável (primeira + 5) é: {variavel_b}")

variaveis_dependentes()